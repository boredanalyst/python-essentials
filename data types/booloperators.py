## Boolean operators are operators that allows you to 'compare' Boolean values.

print("Boolean operators")
print("---")
print("The following variables have the following values:")
print("x = True")
print("y = False")
x = True
y = False
print("---")
print("The following are the Boolean operators.")
print("AND: Only true if BOTH values are TRUE.")
print("OR: Only FALSE if BOTH values are FALSE.")
print("NOT: Reverses the truth value to the opposite.")
print("---")
print(f'AND: x AND y = {x and y}')
print(f'OR: x OR y = {x or y}')
print(f'AND: NOT x = {not x}')

