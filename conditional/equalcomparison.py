## We use <= and => to identify whether the values are greater than, less than, or equal to another number.

varA = 12
varB = 23
varC = 12

if varA <= varB:
    print("The first number is less than the second number")
elif varB >= varA:
    print("The second number is greater than the second number.")


if varA >= varC:
    print("The first number is greater than or equal to the second number.")
else:
    print("The second number is greater than or equal to the first number.")
